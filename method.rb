#メソッド 複数の処理を１つにまとめて扱いやすくしたもの
   #メソッドは大体.の後に打つ
#length 文字列型に使用すると文字数を数えてくれる
puts "WEBCAMP".length

#reverse 文字列を逆に表示する
puts "WEBCAMP".reverse

#include? 引数に指定した文字列が含まれているかどうかを調べる
puts "WEBCAMPでプログラミング学習".include?("WEBCAMP")

#methods 使用可能なメソッドを一覧で表示できる
puts "WEBCAMPでプログラミング学習".methods

#upcase 大文字に変換
puts "webcamp".upcase

#downcase 小文字に変換
puts "WEBCAMP".downcase

